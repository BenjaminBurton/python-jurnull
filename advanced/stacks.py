# Stacks:

# A stack is a collection of elements that follows the Last In, First Out (LIFO) principle. You can use lists in Python to implement a stack.

# Example: Stack Implementation using Lists
stack = []

# Push elements onto the stack
stack.append(1)
stack.append(2)
stack.append(3)

# Pop elements from the stack
print(stack.pop()) # Output: 3
